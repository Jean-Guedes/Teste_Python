indice = 13 # Inicialização da variável 'indice'.
soma = 0 # Inicialização da variável 'soma'.
k = 0  # Inicialização da variável 'k'.

while k < indice: # Enquanto 'k' estiver menor que 'indice', 'k' será incrementado em 1, e 'soma' em 'k'.
    k += 1
    soma += k
 
print(soma) # Imprimir 'soma', o resultado final será 91.